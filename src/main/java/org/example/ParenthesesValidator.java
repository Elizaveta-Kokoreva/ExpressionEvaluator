package org.example;

import java.util.List;

/**
 * Класс предназначен для проверки корректности расстановки скобок
 * в арифметическом выражении.
 *
 * <p>На вход подаётся список токенов, полученный
 * после разбиения строки выражения </p>
 *
 * <p>Проверяются следующие условия:
 * <ul>
 *     <li>каждая открывающая скобка имеет соответствующую закрывающую;</li>
 *     <li>нет закрывающей скобки без ранее открытой;</li>
 * </ul>
 */
public class ParenthesesValidator {

    /**
     * Проверяет корректность расстановки скобок в списке токенов.
     *
     * @param tokens список токенов (числа, операторы, скобки)
     * @throws RuntimeException если скобки расставлены некорректно
     */
    public void validate(List<String> tokens) {
        int balance = 0; // "баланс" открытых и закрытых скобок

        for (String token : tokens) {
            if (token.equals("(")) {
                balance++;
            } else if (token.equals(")")) {
                balance--;

                // Если баланс стал отрицательным — закрывающая скобка раньше открывающей
                if (balance < 0) {
                    throw new RuntimeException("Ошибка: лишняя закрывающая скобка");
                }
            }
        }

        // Если в конце баланс не 0 — есть незакрытые скобки
        if (balance != 0) {
            throw new RuntimeException("Ошибка: несбалансированные скобки");
        }
    }
}
